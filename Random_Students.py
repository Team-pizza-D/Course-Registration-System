# ====== Grade scale ======
def score_to_letter(score: int) -> str:
    if score >= 95:
        return "A+"
    elif score >= 90:
        return "A"
    elif score >= 85:
        return "B+"
    elif score >= 80:
        return "B"
    elif score >= 75:
        return "C+"
    elif score >= 70:
        return "C"
    elif score >= 65:
        return "D+"
    else:
        return "D" 

def random_grade():
    score = random.randint(60, 100)
    letter = score_to_letter(score)
    return letter, score

